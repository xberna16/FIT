SELECT
    device.serialnumber,
    device.manufacturer,
    devicetype.name,
    COUNT(repair.device) AS count_device
FROM
    device
    JOIN devicetype ON devicetype.id = device.type
    left outer JOIN repair ON device.id = repair.device
where exists (select * from repair where device.id = repair.device)
GROUP BY
    device.serialnumber,
    device.manufacturer,
    devicetype.name;
    
    
    
    
    
    
    
    
