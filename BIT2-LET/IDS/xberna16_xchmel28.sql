SELECT
    device.serialnumber,
    device.manufacturer,
    devicetype.name,
    min(repair.reported) AS min_reported
FROM
    device
    INNER JOIN repair ON device.id = repair.device
    INNER JOIN devicetype ON devicetype.id = device.type
GROUP BY
    device.serialnumber,
    device.manufacturer,
    devicetype.name,
    repair.device;
    
    
    
    
